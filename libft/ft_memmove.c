#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t size)
{
	char	*d;
	char	*s;

	d = (char *)dest;
	s = (char *)src;
	if (dest == src)
		return (dest);
	if (s < d)
	{
		while (size--)
			*(d + size) = *(s + size);
		return (dest);
	}
	while (size--)
		*d++ = *s++;
	return (dest);
}
